# include <stdio.h>
int main()
{
    int i;
    int number, sum = 0;

    for(i=1; i <= 10; ++i)
    {
        printf("Enter a n%d: ",i);
        scanf("%d",&number);

        // If user enters negative number, loop is terminated
        if(number < 0)
        {
            continue;
        }

        sum += number; // sum = sum + number;
    }

    printf("Sum = %d",sum);
    
    return 0;
}

